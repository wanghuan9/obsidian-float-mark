export type I18nKey = keyof typeof TRANSLATIONS["zh-CN"];

export function translate(language: PluginLanguage | undefined, key: I18nKey, params: TranslationParams = {}): string {
	const normalizedLanguage = normalizePluginLanguage(language, "zh-CN");
	const template = TRANSLATIONS[normalizedLanguage][key] || TRANSLATIONS["zh-CN"][key] || key;
	return template.replace(/\{(\w+)}/g, (match, name: string) => String(params[name] ?? match));
}

export function createTranslator(settings: Pick<SideMarkSettings, "language">): (key: I18nKey, params?: TranslationParams) => string {
	return (key, params) => translate(settings.language, key, params);
}

export function normalizePluginLanguage(value: unknown, fallback: PluginLanguage): PluginLanguage {
	return value === "en" || value === "zh-CN" ? value : fallback;
}

export function getDefaultCommentAuthorName(language: PluginLanguage): string {
	return language === "en" ? "Me" : "我";
}

export function getInitialPluginLanguage(app: App, currentObsidianLanguage = ""): PluginLanguage {
	const language = currentObsidianLanguage || getObsidianLanguage(app);
	return isChineseLanguage(language) ? "zh-CN" : "en";
}

export function getObsidianLanguage(app: App): string {
	const vaultWithConfig = app.vault as typeof app.vault & { getConfig?: (key: string) => unknown };
	const configuredLanguage = vaultWithConfig.getConfig?.("userLanguage");
	if (typeof configuredLanguage === "string" && configuredLanguage.trim()) {
		return configuredLanguage;
	}
	const appWithLocale = app as App & { locale?: unknown };
	return typeof appWithLocale.locale === "string" ? appWithLocale.locale : "";
}

export function isChineseLanguage(language: string): boolean {
	const normalized = language.toLowerCase();
	return normalized === "zh" || normalized.startsWith("zh-") || normalized.startsWith("zh_");
}
